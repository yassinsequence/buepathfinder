// Helper function to filter by category
export function getCareersByCategory(category: string): CareerOpportunity[] {
  return egyptianCareers.filter(career => career.category === category);
}

// Helper function to get all jobs (excluding postgraduate)
export function getAllJobs(): CareerOpportunity[] {
  return egyptianCareers.filter(career => career.type === 'job');
}

// Helper function to get all postgraduate programs
export function getAllPostgraduate(): CareerOpportunity[] {
  return egyptianCareers.filter(career => career.type === 'postgraduate');
}

// Helper function to search careers
export function searchCareers(query: string): CareerOpportunity[] {
  const lowerQuery = query.toLowerCase();
  return egyptianCareers.filter(career =>
    career.title.toLowerCase().includes(lowerQuery) ||
    career.organization.toLowerCase().includes(lowerQuery) ||
    career.category.toLowerCase().includes(lowerQuery) ||
    career.requiredSkills.some(skill => skill.toLowerCase().includes(lowerQuery))
  );
}
